#include "libft.h"

void	ft_list_reverse(t_list **begin_list)
{
	t_list *next;
	t_list *current;
	t_list *prev;

	if (*begin_list == NULL || begin_list == NULL)
		return ;
	current = *begin_list;
	prev = NULL;
	while (current != NULL)
	{
		next = current->next;
		current->next = prev;
		prev = current;
		current = next;
	}
	*begin_list = prev;
}
